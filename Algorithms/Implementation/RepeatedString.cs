class Result
{

    /*
     * Complete the 'repeatedString' function below.
     *
     * The function is expected to return a LONG_INTEGER.
     * The function accepts following parameters:
     *  1. STRING s
     *  2. LONG_INTEGER n
     */

    public static long repeatedString(string s, long n)
    {
        char letterA = 'a';

        if (!s.Contains(letterA))
            return 0;

        long left = n % s.Length;
        long division = n / s.Length;

        string leftString = s.Substring(0, (int)left);

        int countOfA = s.Count(c => c == letterA);
        int aCountLeftString = leftString.Count(c => c == letterA);

        return (countOfA * division) + aCountLeftString;
    }

}

class Solution
{
    public static void Main(string[] args)
    {
        TextWriter textWriter = new StreamWriter(@System.Environment.GetEnvironmentVariable("OUTPUT_PATH"), true);

        string s = Console.ReadLine();

        long n = Convert.ToInt64(Console.ReadLine().Trim());

        long result = Result.repeatedString(s, n);

        textWriter.WriteLine(result);

        textWriter.Flush();
        textWriter.Close();
    }
}
